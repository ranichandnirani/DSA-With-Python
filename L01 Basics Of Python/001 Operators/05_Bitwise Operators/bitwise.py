# All Bitwise Operators 

a = 5   # binary: 0101
b = 3   # binary: 0011

print("a & b  =", a & b)    # 1  → 0001
print("a | b  =", a | b)    # 7  → 0111
print("a ^ b  =", a ^ b)    # 6  → 0110
print("~a     =", ~a)       # -6
print("a << 1 =", a << 1)   # 10 → 1010
print("a >> 1 =", a >> 1)   # 2  → 0010
